#include "Robot.hpp"

int Robot::robotCounter = 0;


/**************************************************************************************************************/
// CONSTRUCTORS
Robot::Robot(int x, int y) : Entity(x,y)
{
	symbol = 'R';
	id = ++robotCounter;
}
/**************************************************************************************************************/


/**************************************************************************************************************/
// GET INFO
int Robot::getID() const
{
	return id;
}
