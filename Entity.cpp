#include "Entity.hpp"


/**************************************************************************************************************/
// CONSTRUCTORS
Entity::Entity(){

}

Entity::Entity(int x, int y){
	pos = {x, y};
	this->alive = true;
}
/**************************************************************************************************************/


/**************************************************************************************************************/
// GET INFO
Position Entity::getPos() const
{
	return pos;
}

char Entity::getSymbol() const
{
	return symbol;
}

bool Entity::isAlive() const
{
	return alive;
}
/**************************************************************************************************************/


/**************************************************************************************************************/
// ACTIONS
void Entity::kill()
{
	alive = false;
	symbol = tolower(symbol);
}

void Entity::setPos(Position newPos)
{
	pos = newPos;
}
